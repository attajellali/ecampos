package pe.gob.pcm.constitucion.web.bean;

public class Parametro {

	private String codigoParametro;
	private String descripcionParametro;
	private String descripconCortaParametro;
	
	public String getCodigoParametro() {
		return codigoParametro;
	}
	public void setCodigoParametro(String codigoParametro) {
		this.codigoParametro = codigoParametro;
	}
	public String getDescripcionParametro() {
		return descripcionParametro;
	}
	public void setDescripcionParametro(String descripcionParametro) {
		this.descripcionParametro = descripcionParametro;
	}
	public String getDescripconCortaParametro() {
		return descripconCortaParametro;
	}
	public void setDescripconCortaParametro(String descripconCortaParametro) {
		this.descripconCortaParametro = descripconCortaParametro;
	}
	
	
	
}
